#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - fun to add two int
 * @a: first value
 * @b: second value
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - fun to sub two int
 * @a: first value
 * @b: second value
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - fun to multiply two int
 * @a: first value
 * @b: second value
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - fun to divied two int
 * @a: first value
 * @b: second value
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - fun to add two int
 * @a: first value
 * @b: second value
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
